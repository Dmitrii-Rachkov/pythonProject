# Записываем данные в фигурных скобках {}
data = {1, "str", 8.8}
print(data, type(data))
# множества выводятся в хаотичном порядке

# Можно другие типы данных переводить в множества
spisok = [0, 1, 2, 3]
print(spisok, type(spisok))
data_2 = set(spisok)
