# Во множествах не поддерживаются слайсы

# Добавить элемент к множеству:
data.add(5)
print(data)

# Удаление по значению указанного элемента если он есть в множестве:
data.discard(8.8)
print(data)
# Если элемента по значению не будет во множестве, то никакой ошибки не будет
data.discard(2)
print(data)

# Можно удалить элемент по значению ещё одним способом, но если элемент не найден то будет ошибка:
data.remove(2)
print(data)




