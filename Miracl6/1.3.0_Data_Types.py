# Типы данных

# int - целое число
a = 5
print(f"values: {a = }, {type(a) = }")

# float - число с плавающей точкой
b = 5.5
print(f"{b = }, {type(b) = }")

# str - строка
c = "Hello"
print(f"{c = }, {type(c) = }")

# bool - логический тип данных True или False
d = False
print(f"{d = }, {type(d) = }")




